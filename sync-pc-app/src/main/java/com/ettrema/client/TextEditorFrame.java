    @Action
    public void save() {
        String s = this.editor.getText();
        ByteArrayInputStream in = new ByteArrayInputStream( s.getBytes() );
        try {
            this.file = this.file.parent.upload( file.name, in, (long) s.length(), null );
        } catch( Exception ex ) {
            log.error( "", ex);
            JOptionPane.showMessageDialog( null, "Failed to save file", "Save failed", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_mnuSaveMouseClicked

