    private void load() {
        ByteArrayOutputStream out = new ByteArrayOutputStream();
        try {
            file.download( out, null );
        } catch( CancelledException ex ) {
            JOptionPane.showMessageDialog( null, "Open file cancelled", "Cancelled", JOptionPane.ERROR_MESSAGE);
        } catch( HttpException ex ) {
            JOptionPane.showMessageDialog( null, "Failed to open file", "Open failed", JOptionPane.ERROR_MESSAGE);
        }
        this.editor.setText( out.toString() );
    }

